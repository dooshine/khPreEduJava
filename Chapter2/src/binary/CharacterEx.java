package binary;

public class CharacterEx {
	public static void main(String[] args) {
		
		char ch = 'A';
		
		System.out.println("ch: " + ch);
		System.out.println("(int)ch: " + (int)ch);
		
		ch = 66;
		
		System.out.println("ch: " + ch);
		
		int ch2 = 67;
		System.out.println("ch2: " + ch2);
		System.out.println("(char)ch2: " + (char)ch2);
	}
}
