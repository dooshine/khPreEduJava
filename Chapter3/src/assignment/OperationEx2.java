package assignment;

public class OperationEx2 {
	public static void main(String[] args) {
		int num = 10;
		System.out.println("num: " + num);
		System.out.println("num++: " + num++);
		System.out.println("num: " + num);
		
		int num2 = 10;
		System.out.println("num2: " + num2);
		System.out.println("++num2: " + ++num2);
		System.out.println("num2: " + num2);
		
		System.out.println(3<5);
		
		int num3 = 10;
		int num4 = 5;
		boolean flag = num3 > num4;
		System.out.println("flag: " + flag);
	}
}
